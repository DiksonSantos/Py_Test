class cal():

    def __init__(self, a, b):
        self.Alpha = a
        self.Bravo = b

    def add(self):
        return self.Alpha + self.Bravo

    def mul(self):
        return self.Alpha * self.Bravo

    def div(self):
        return self.Alpha / self.Bravo

    def sub(self):
        return self.Alpha - self.Bravo

